package main

import "fmt"

func main() {
	var a, b int = 20, 4

	//addition
	sum := a + b
	fmt.Println("Sum:", sum)

	//substraction
	difference := a - b
	fmt.Println("difference:", difference)

	//multiplication
	product := a * b
	fmt.Println("product:", product)

	//division
	quotient := a / b
	fmt.Println("quotient:", quotient)
}
