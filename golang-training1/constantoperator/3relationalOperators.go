package main

import "fmt"

func main() {
	var a, b int = 10, 5

	//equal to
	fmt.Println(a == b)

	//not equal to
	fmt.Println(a != b)

	//greater than
	fmt.Println(a > b)

	//less than
	fmt.Println(a < b)

	//greater than or equal to
	fmt.Println(a >= b)

	//less than or equal to
	fmt.Println(a <= b)
}
